package basics;

public class PrimitivesAndObjects {

    public static void main(String[] args){

        String name = new String("Sachin Dahal"); // object
        String age = "19 years old"; // primitive
        System.out.println("I am "+name+ ". I am "+age);

        int a = 1; // primitive
        Integer b = 2; // object
        System.out.println(b);
    }
}
